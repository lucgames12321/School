print ("Ik heb kaas nodig. Welke kaas is lekker? (Kies uit: Jonge, Oude of Geiten)")

keuze = input()

if keuze == "Jonge":
    print("Jonge kaas is inderdaar verrukkelijk")

elif keuze == "Oude":
    print("Oude kaas is smikkeltastisch")

elif keuze == "Geiten":
    print("Geiten kaas? Wie eet dit nou?")

else: 
    print("Je hebt misschien een typo gemaakt. (Misschien hoofdletter vergeten....)")
